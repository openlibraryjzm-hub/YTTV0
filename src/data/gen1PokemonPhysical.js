/**
 * Gen 1 Pokémon physical data: height (m), weight (kg), gender ratio.
 * Source: PokeAPI (pokemon.csv: height in dm, weight in hg; pokemon_species.csv: gender_rate).
 * atlas/gen1-pokemon-reference.md — Physical data section.
 *
 * gender_rate: -1 = genderless, 0 = 100% male, 8 = 100% female, 1–7 = 12.5%–87.5% female;
 *              some species use 0–255 scale (female chance in 256ths) — we normalize to a label.
 * @type {{ height: number, weight: number, genderRatio: string }[]} 1-indexed by id
 */
const genderRateToLabel = (rate) => {
    if (rate === -1) return 'Genderless';
    if (rate === 0) return '100% ♂';
    if (rate === 8) return '100% ♀';
    if (rate >= 1 && rate <= 7) {
        const pct = rate * 12.5;
        return `${pct}% ♀ / ${100 - pct}% ♂`;
    }
    const pct = Math.round((rate / 256) * 100);
    if (pct <= 0) return '100% ♂';
    if (pct >= 100) return '100% ♀';
    return `${pct}% ♀ / ${100 - pct}% ♂`;
};

